#include "test.hpp"

void    title(std::string name)
{
    std::string str = "*";
    int n;

    n = 96 - name.size();
    if (n % 2 == 0)
         str.insert(str.end(), n / 2 - 1, ' ');
    else
         str.insert(str.end(), n / 2 , ' ');
    str += name;
    str.insert(str.end(), n / 2, ' ');
    str += "*"
;    std::cout  << "\033[33;1m"
			    << "*************************************************************************************************" << std::endl
				<< str << std::endl
				<< "*************************************************************************************************" << std::endl
				<< "\033[0m" << std::endl;
}
